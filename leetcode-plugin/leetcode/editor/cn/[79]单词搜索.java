class Solution {

    public boolean exist(char[][] board, String word) {
        int m = board.length;
        int n = board[0].length;
        for (int i = 0; i < m; i++) {
            for (int j = 0; j < n; j++) {
                if (dfs(board, word, i * n + j, 0)) {
                    return true;
                }
            }
        }
//        for (int i = 0; i < m; i++) {
//            System.out.println(Arrays.toString(board[i]));
//        }
        return false;

    }

    boolean dfs(char[][] board, String word, int idx, int l) {

        int m = board.length;
        int n = board[0].length;

        int i = idx / n;
        int j = idx % n;
//        System.out.println(board[i][j]);
        if (word.charAt(l) != board[i][j]) {
            return false;
        }
        char c = board[i][j];
        board[i][j] = '0';
        if (l == word.length() - 1) {
            board[i][j] = c;
            return true;
        }
        int[] dx = new int[]{1, -1, 0, 0};
        int[] dy = new int[]{0, 0, 1, -1};
        for (int k = 0; k < 4; k++) {
            int x = i + dx[k];
            int y = j + dy[k];
            int d = x * n + y;
            if (x < 0 || x >= m || y < 0 || y >= n) {
                continue;
            }
            if (board[x][y] != '0') {
                if (dfs(board, word, d, l + 1)) {
                    board[i][j] = c;
                    return true;
                }
            }
        }
        board[i][j] = c;
        return false;
    }
}
