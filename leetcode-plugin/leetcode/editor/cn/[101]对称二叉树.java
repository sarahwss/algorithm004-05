import java.util.ArrayDeque;
import java.util.Deque;
import java.util.TreeMap;

/**
 * Definition for a binary tree node.
 * public class TreeNode {
 * int val;
 * TreeNode left;
 * TreeNode right;
 * TreeNode() {}
 * TreeNode(int val) { this.val = val; }
 * TreeNode(int val, TreeNode left, TreeNode right) {
 * this.val = val;
 * this.left = left;
 * this.right = right;
 * }
 * }
 */
class Solution {

    public boolean isSymmetric(TreeNode root) {
        Deque<TreeNode> queue = new ArrayDeque<>();
        if (root.left == null && root.right == null) {
            return true;
        }
        if (root.left == null || root.right == null) {
            return false;
        }
        //æ³¨æ„åŠ è¿›queueçš„ä¸èƒ½æ˜¯null
        queue.offerLast(root.left);
        queue.offerLast(root.right);
        while (!queue.isEmpty()) {
            TreeNode node1 = queue.pollFirst();
            TreeNode node2 = queue.pollFirst();
            if (node1.val != node2.val) {
                return false;
            }
            //æ³¨æ„åŠ è¿›queueçš„ä¸èƒ½æ˜¯null
            if (node1.left != null && node2.right == null || node1.left == null && node2.right != null ||
                    node1.right != null && node2.left == null || node1.right == null && node2.left != null) {
                return false;
            }
            if (node1.left != null) {
                queue.offerLast(node1.left);
                queue.offerLast(node2.right);
            }

            if (node1.right != null) {
                queue.offerLast(node1.right);
                queue.offerLast(node2.left);
            }

        }
        return true;
    }


    public boolean isSymmetric(TreeNode left, TreeNode right) {
        if (left == null && right == null) {
            return true;
        }
        if (left == null || right == null) {
            return false;
        }
        return left.val == right.val && isSymmetric(left.left, right.right) && isSymmetric(left.right, right.left);
    }
}

class TreeNode {

    int val;

    TreeNode left;

    TreeNode right;

    TreeNode() {
    }

    TreeNode(int val) {
        this.val = val;
    }

    TreeNode(int val, TreeNode left, TreeNode right) {
        this.val = val;
        this.left = left;
        this.right = right;
    }
}
