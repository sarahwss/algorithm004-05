import java.util.ArrayList;
import java.util.HashMap;
import java.util.List;
import java.util.Map;

/**
 * Definition for a binary tree node.
 * public class TreeNode {
 * int val;
 * TreeNode left;
 * TreeNode right;
 * TreeNode() {}
 * TreeNode(int val) { this.val = val; }
 * TreeNode(int val, TreeNode left, TreeNode right) {
 * this.val = val;
 * this.left = left;
 * this.right = right;
 * }
 * }
 */
class Solution {

    public int pathSum(TreeNode root, int targetSum) {
        // æ€»å’Œæœ‰å¯èƒ½æº¢å‡ºï¼Œkeyè®¾ç½®ä¸ºlong
        Map<Long, Integer> sumCount = new HashMap<>();
        sumCount.put(0L, 1);// è¿™ä¸€æ­¥ä¸èƒ½å°‘ï¼Œå¦åˆ™ä¼šå°‘å¤´èŠ‚ç‚¹
        return pathSum(root, targetSum, sumCount, 0);
    }

    public int pathSum(TreeNode root, int targetSum, Map<Long, Integer> sumCount, long sum) {
        if (root == null) {
            return 0;
        }
        int count = 0;
        sum += root.val;
        //        System.out.println(root.val + " " + sum);
        long key = sum - targetSum;
        if (sumCount.getOrDefault(key, 0) != 0) {
            count += sumCount.get(key);
        }
        sumCount.put(sum, sumCount.getOrDefault(sum, 0) + 1);
        count += pathSum(root.left, targetSum, sumCount, sum);
        count += pathSum(root.right, targetSum, sumCount, sum);
        sumCount.put(sum, sumCount.getOrDefault(sum, 0) - 1);
        return count;
    }
}

class TreeNode {

    int val;

    TreeNode left;

    TreeNode right;

    TreeNode() {
    }

    TreeNode(int val) {
        this.val = val;
    }

    TreeNode(int val, TreeNode left, TreeNode right) {
        this.val = val;
        this.left = left;
        this.right = right;
    }
}
